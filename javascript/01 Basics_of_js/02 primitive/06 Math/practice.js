console.log(Number("0b101")); // 5 (binary to decimal)
console.log(Number("0o377")); // 255 (octal to decimal)
console.log(Number("0xFF")); // 255 (hexadecimal to decimal)

console.log(parseInt("101", 2)); // 5 (binary to decimal)
console.log(parseInt("377", 8)); // 255 (octal to decimal)
console.log(parseInt("FF", 16)); // 255 (hexadecimal to decimal)
