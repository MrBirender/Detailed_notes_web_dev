// -----------------------------------
// JavaScript Math Library Methods
// -----------------------------------

// Absolute value
console.log(Math.abs(-5)); // 5

// Rounding numbers
console.log(Math.round(4.5)); // 5 (round to nearest integer)
console.log(Math.ceil(4.2)); // 5 (round up)
console.log(Math.floor(4.8)); // 4 (round down)
console.log(Math.trunc(4.8)); // 4 (removes decimal part)

// Generating random numbers
console.log(Math.random()); // Random number between 0 (inclusive) and 1 (exclusive)
console.log(Math.random() * 10); // Random number between 0 and 10

// Maximum and minimum of a set of numbers
console.log(Math.max(1, 2, 3)); // 3
console.log(Math.min(1, 2, 3)); // 1

// Power and square root
console.log(Math.pow(2, 3)); // 8 (2^3)
console.log(Math.sqrt(16)); // 4

// Trigonometric functions
console.log(Math.sin(Math.PI / 2)); // 1
console.log(Math.cos(0)); // 1

// Binary, octal, and hexadecimal conversions
console.log(Number("101", 2)); // 5 (binary to decimal)
console.log(Number("377", 8)); // 255 (octal to decimal)
console.log(Number("FF", 16)); // 255 (hexadecimal to decimal)


